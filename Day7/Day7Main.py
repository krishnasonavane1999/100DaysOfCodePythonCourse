total_words = len(word_list)

pick_random_word_idx = random.randint(0, (total_words - 1))

pick_random_word = word_list[pick_random_word_idx]

# print("Randomly picked word: " + pick_random_word)

pick_random_word_len = len(pick_random_word)

# print("Randomly picked word length: ", pick_random_word_len)

char_to_guess = ''
dashed_word = list("")
hangman = 0

for i in range(pick_random_word_len):
  dashed_word += '_'

traverse = pick_random_word_len

print(''.join(dashed_word))
print(pick_random_word)

while traverse:
  char_to_guess = input("Guess the character: ")
  is_char_persent = -1
  for i in range(pick_random_word_len):
    if char_to_guess == pick_random_word[i]:
      dashed_word[i] = char_to_guess
    else:
      is_char_persent = -1

  if is_char_persent == -1:
    hangman += 1

  print(''.join(dashed_word))
    
  if hangman == 7:
    print(hangman_stages.stages[-hangman])
    print(game_over_art.game_over)
    break
  elif is_char_persent == -1:
    print(hangman_stages.stages[-hangman])
    print("Hangman: ",hangman)
  
  if ''.join(dashed_word).find('_') == -1:
    print(you_won_art.you_won)
  

traverse -=1

